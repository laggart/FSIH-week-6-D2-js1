let color = prompt("Escoje un color: azul, verde, rojo, amarillo o violeta")

if(color == "azul") {
    document.write("<h1>Azulada hortensia que llora rocío</h1>")
} else if (color == "verde") {
    document.write("<h1>verde que te quiero verde<h1>")
} else if (color == "rojo") {
    document.write("<h1>roja es la manzana, rojo el corazón</h1>")
} else if (color == "amarillo") {
    document.write("<h1>amarillo es el sol, amarillo el limón...</h1>")
} else if (color == "violeta") {
    document.write("<h1>El balde es color violeta, el aro es color de fuego. Vienen a verlas pasar...</h1>")
} else {
    document.write("<h2>no elegiste ninguno de los valores  ")
}